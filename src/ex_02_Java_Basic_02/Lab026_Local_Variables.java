package ex_02_Java_Basic_02;

public class Lab026_Local_Variables {
    public static void main(String[] args){
        // %d -> int, byte, long, short, - data type
        // %s -> String
        // %f -> float, double,
        // %b ->boolean
        int a= 10;
        String ss = "vikash";
        float f = 57282.6763f;
        boolean married = false;

        System.out.printf("integer value id %d,string value is %s,  float value is %f, boolean value is %b ", a, ss, f, married);

    }
}
