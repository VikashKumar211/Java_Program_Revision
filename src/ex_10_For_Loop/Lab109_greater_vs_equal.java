package ex_10_For_Loop;

public class Lab109_greater_vs_equal {
    public static void main(String[] args) {
        int i , j;

        for (i=0 ; i<=5; i++ ){
            System.out.println(i);
        }
        System.out.println("----");

        for (j = 0; j>= -5 ; j--){
            System.out.println(j);
        }

    }
}
