package ex_04_operators;

public class Lab040_Relational_Operators {
    public static void main(String[] args) {
        int a =10, b =15;
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal
        boolean c = a>b;
        boolean c1 = a==b;

        System.out.println(c);
        System.out.println(c1);
    }
}
