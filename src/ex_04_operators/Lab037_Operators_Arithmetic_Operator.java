package ex_04_operators;

public class Lab037_Operators_Arithmetic_Operator {
    public static void main(String[] args) {
          /*
                + (Addition)
               - (Subtraction)
               * (Multiplication)
               / (Division)
               % (Modulus) | Modulus - Remainder
         */
        int a = 10, b = 20, c= 30;
        System.out.println(a+b+c);
        System.out.println(a-b-c);
        System.out.println(a*b-c);
        System.out.println(b/a);
        System.out.println(b%a);
    }
}
