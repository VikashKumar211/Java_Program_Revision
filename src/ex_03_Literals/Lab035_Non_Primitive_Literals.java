package ex_03_Literals;

public class Lab035_Non_Primitive_Literals {
    public static void main(String[] args) {

        //primitive date type -> defined by JAVA
        int age = 25;
        char abc = 'A';

        //Non primitive date type -> defined by user
        String name  = "Vikash";
        int[] arrayNew = new int[10];
        System.out.println(abc);
        System.out.println(age);
        System.out.println(name);
        System.out.println(arrayNew); //[I@27716f4


    }
}
